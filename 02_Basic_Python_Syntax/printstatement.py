# The print() function in Python is used to display output to the console.

# Printing a simple string
print("Hello, World!")

# Printing multiple values separated by commas
name = "Alice"
age = 25
print("Name:", name, "Age:", age)

# Using the 'sep' parameter to change the separator between values
print("Python", "is", "fun", sep="-")

# Using the 'end' parameter to change what is printed at the end (default is newline)
print("This is printed on", end=" ")
print("the same line.")

# Printing the result of an expression
print("2 + 3 =", 2 + 3)
