# Boolean
is_true_flag = True
is_false_flag = False
print("True Flag:", is_true_flag, ", False Flag:", is_false_flag)  # True Flag: True , False Flag: False

# Boolean with comparison operators
number1 = 10
number2 = 20
print("Is number1 equal to number2?", number1 == number2)  # Is number1 equal to number2? False
print("Is number1 less than number2?", number1 < number2)  # Is number1 less than number2? True
