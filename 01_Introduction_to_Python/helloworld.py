# Example: Hello World in Python

def print_hello_world():
    message = "Hello, World!"
    print(message)

if __name__ == "__main__":
    print_hello_world()

# Output:
# Hello, World!
#
# In Python, printing to the console is simple and does not require defining a class, but you can use functions for better structure.
